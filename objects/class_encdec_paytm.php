function ct_verifychecksum_e($arrayList, $key, $checksumvalue) {
	$arrayList = ct_removeCheckSumParam($arrayList);
	ksort($arrayList);
	$str = ct_getArray2StrForVerify($arrayList);
	$paytm_hash = ct_decrypt_e($checksumvalue, $key);
	$salt = substr($paytm_hash, -4);

	$finalString = $str . "|" . $salt;

	$website_hash = hash("sha256", $finalString);
	$website_hash .= $salt;

	$validFlag = "FALSE";
	if ($website_hash == $paytm_hash) {
		$validFlag = "TRUE";
	} else {
		$validFlag = "FALSE";
	}
	return $validFlag;
}

