function ct_getArray2Str($arrayList) {
	$findme   = 'REFUND';
	$findmepipe = '|';
	$paramStr = "";
	$flag = 1;	
	foreach ($arrayList as $key => $value) {
		$pos = strpos($value, $findme);
		$pospipe = strpos($value, $findmepipe);
		if ($pos !== false || $pospipe !== false) 
		{
			continue;
		}
		
		if ($flag) {
			$paramStr .= ct_checkString_e($value);
			$flag = 0;
		} else {
			$paramStr .= "|" . ct_checkString_e($value);
		}
	}
	return $paramStr;
}

